# sep 分割符" "
# end 结束以后的默认输出\n
# 一个或多个值使用,来分割
# ,表示当前的sep符
# \n 换行符

# 格式化打印
# print(value, ..., sep=' ', end='\n', file=sys.stdout, flush=False)
print('hello', 'boy' '2020' 'goon')
print('hello', 'boy' '2020' 'goon', sep='+')
print('hello', 'boy' '2020' 'goon', sep='+', end='==='"\n")
print("haha")
print()
